class LegalDB:
    """法律法规数据库"""

    def __init__(self):
        self.articles = LAW_ARTICLES
        self.cases = CASES
        self._build_index()

    def _build_index(self):
        """构建关键词索引"""
        self.keyword_index = {}
        for i, art in enumerate(self.articles):
            for kw in art["keywords"]:
                if kw not in self.keyword_index:
                    self.keyword_index[kw] = []
                self.keyword_index[kw].append(i)

    def search_articles(self, keyword: str) -> list:
        """按关键词搜索法律条款"""
        results = []
        keyword = keyword.lower()
        for art in self.articles:
            if (keyword in art["content"].lower()
                or keyword in art["keywords"]
                or keyword in art["article_no"].lower()
                or keyword in art["law_name"].lower()):
                results.append(art)
        return results

    def search_by_category(self, category: str) -> list:
        """按分类检索"""
        return [a for a in self.articles if a["category"] == category]

    def search_cases(self, keyword: str) -> list:
        """搜索判例"""
        keyword = keyword.lower()
        results = []
        for c in self.cases:
            if (keyword in c["title"].lower()
                or keyword in c["key_issue"].lower()
                or keyword in c["summary"].lower()
                or keyword in c["cause"].lower()):
                results.append(c)
        return results

    def get_article(self, article_no: str) -> Optional[dict]:
        """按条款编号精确查找"""
        for a in self.articles:
            if a["article_no"] == article_no:
                return a
        return None

    def get_case(self, case_id: str) -> Optional[dict]:
        for c in self.cases:
            if c["case_id"] == case_id:
                return c
        return None

    def find_related_articles(self, keywords: list) -> list:
        """根据关键词列表查找相关法条"""
        seen = set()
        results = []
        for kw in keywords:
            for art in self.search_articles(kw):
                if art["article_no"] not in seen:
                    seen.add(art["article_no"])
                    results.append(art)
        return results

    def find_related_cases(self, keywords: list) -> list:
        """根据关键词查找相关判例"""
        seen = set()
        results = []
        for kw in keywords:
            for case in self.search_cases(kw):
                if case["case_id"] not in seen:
                    seen.add(case["case_id"])
                    results.append(case)
        return results

    def get_categories(self) -> dict:
        return CATEGORIES

    def to_json(self):
        return {
            "articles": self.articles,
            "cases": self.cases,
            "categories": CATEGORIES
        }


